export function renderTableOfContents(sections: FilterSection[]): string {
  return [
    "#===============================================================================================================",
    "# [WELCOME] TABLE OF CONTENTS + QUICKJUMP TABLE",
    "#===============================================================================================================",
    ...sections.map((section) => `# [[${section.code}]] ${section.title}`),
    ""
  ].join("\n");
}

export function renderSection(section: FilterSection): string {
  const rules = section.rules.map(renderRule).filter(Boolean).join("\n\n");
  return [
    "#===============================================================================================================",
    `# [[${section.code}]] ${section.title}`,
    "#===============================================================================================================",
    section.waypoint ? `# !! Waypoint ${section.waypoint} : "${section.title}" : "${section.titleTw}"` : "",
    "",
    rules
  ].filter((line, index) => line !== "" || index > 2).join("\n");
}

export function generateFilter(sections: FilterSection[], options: { author: string; strictness: string; style: string; version: string }): string {
  return [
    renderHeader(options.author, options.strictness, options.style, options.version),
    renderTableOfContents(sections),
    ...sections.map(renderSection),
    ""
  ].join("\n");
}

function valueForItem(item: SimulationItem, keyword: string): string | number | boolean | undefined {
  const map: Record<string, keyof SimulationItem> = {
    BaseType: "baseType",
    Class: "itemClass",
    Rarity: "rarity",
    AreaLevel: "areaLevel",
    ItemLevel: "itemLevel",
    DropLevel: "dropLevel",
    StackSize: "stackSize",
    Quality: "quality",
    Sockets: "sockets",
    GemLevel: "gemLevel",
    WaystoneTier: "waystoneTier",
    UnidentifiedItemTier: "unidentifiedItemTier",
    Corrupted: "corrupted",
    Mirrored: "mirrored",
    Identified: "identified",
    Width: "width",
    Height: "height"
  };
  const key = map[keyword];
  return key ? item[key] : undefined;
}

export function conditionMatches(condition: RuleCondition, item: SimulationItem): boolean {
  const itemValue = valueForItem(item, condition.keyword);
  if ("values" in condition) {
    const values = condition.values;
    let matched = false;
    if (condition.keyword === "BaseType") matched = values.includes(item.baseType);
    else if (condition.keyword === "Class") matched = values.includes(item.itemClass);
    else if (condition.keyword === "Rarity") matched = item.rarity ? values.includes(item.rarity) : false;
    else matched = values.some((value) => String(itemValue ?? "").includes(value));
    return "operator" in condition && condition.operator === "!=" ? !matched : matched;
  }
  if (typeof condition.value === "boolean") {
    return Boolean(itemValue) === condition.value;
  }
  if (typeof itemValue !== "number") return false;
  switch (condition.operator) {
    case ">=":
      return itemValue >= condition.value;
    case "<=":
      return itemValue <= condition.value;
    case ">":
      return itemValue > condition.value;
    case "<":
      return itemValue < condition.value;
    case "=":
      return itemValue === condition.value;
  }
}

export interface SimulationResult {
  matchedRules: FilterRule[];
  finalRule?: FilterRule;
  hidden: boolean;
  rendered: string;
}

export function simulateItem(sections: FilterSection[], item: SimulationItem): SimulationResult {
  const allRules = sections.flatMap((section) => section.rules).filter((rule) => rule.enabled);
  const matchedRules: FilterRule[] = [];

  for (const rule of allRules) {
    const matches = rule.conditions.every((condition) => conditionMatches(condition, item));
    if (!matches) continue;
    matchedRules.push(rule);
    if (!rule.continue) {
      return {
        matchedRules,
        finalRule: rule,
        hidden: rule.directive === "Hide",
        rendered: renderRule(rule)
      };
    }
  }

  const finalRule = matchedRules[matchedRules.length - 1];
  return {
    matchedRules,
    finalRule,
    hidden: finalRule?.directive === "Hide",
    rendered: finalRule ? renderRule(finalRule) : ""
  };
}

export function validateFilterSyntax(filterText: string) {
  const requiredTokens = [
    "Show",
    "Hide",
    "Continue",
    "Class",
    "BaseType",
    "Rarity",
    "SetFontSize",
    "SetTextColor",
    "SetBorderColor",
    "SetBackgroundColor",
    "PlayAlertSound",
    "PlayEffect",
    "MinimapIcon",
    "DisableDropSound",
    "DropLevel"
  ];
  return requiredTokens.map((token) => ({
    token,
    present: filterText.includes(token)
  }));
}
