import type { FilterAction, FilterRule, FilterSection, RuleCondition, SimulationItem } from "../types";

const quote = (value: string) => `"${value.replace(/"/g, '\\"')}"`;

function renderValues(values: string[]) {
  return values.map(quote).join(" ");
}

export function renderCondition(condition: RuleCondition): string {
  if ("operator" in condition && "values" in condition && condition.operator) {
    return `${condition.keyword} ${condition.operator} ${renderValues(condition.values)}`;
  }
  if ("values" in condition) {
    return `${condition.keyword} ${renderValues(condition.values)}`;
  }
  if ("operator" in condition && "value" in condition && typeof condition.value === "number") {
    const op = condition.operator === "=" ? "" : ` ${condition.operator}`;
    return `${condition.keyword}${op} ${condition.value}`;
  }
  return `${condition.keyword} ${condition.value ? "True" : "False"}`;
}

export function renderAction(action: FilterAction): string {
  switch (action.keyword) {
    case "SetFontSize":
      return `SetFontSize ${action.value}`;
    case "SetTextColor":
    case "SetBorderColor":
    case "SetBackgroundColor":
      return `${action.keyword} ${action.value.r} ${action.value.g} ${action.value.b} ${action.value.a}`;
    case "PlayAlertSound":
      return `PlayAlertSound ${action.id} ${action.volume}`;
    case "PlayEffect":
      return `PlayEffect ${action.color}${action.temp ? " Temp" : ""}`;
    case "MinimapIcon":
      return `MinimapIcon ${action.size} ${action.color} ${action.shape}`;
    case "DisableDropSound":
      return `DisableDropSound ${action.value ? "True" : "False"}`;
  }
}

export function renderRule(rule: FilterRule): string {
  if (!rule.enabled) return "";
  const header = `${rule.directive}${rule.tierTag ? ` # ${rule.tierTag}` : ""}`;
  const body = [
    ...rule.conditions.map(renderCondition),
    ...rule.actions.map(renderAction),
    ...(rule.continue ? ["Continue"] : [])
  ].map((line) => `\t${line}`);
  return [header, ...body].join("\n");
}

